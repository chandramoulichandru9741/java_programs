package com.gqt.basicsofjava.datatypes;

public class ExplicitTC1 {

	public static void main(String[] args) {
		long l = 126;
		System.out.println("Long = " + l);
		int i = l;
		System.out.println("Int = " + i);
		short s = i;
		System.out.println("Short = " + s);
		byte b = s;
		System.out.println("Byte = " + b);
		
		
		
	}

}
